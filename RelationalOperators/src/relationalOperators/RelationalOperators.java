package relationalOperators;

public class RelationalOperators {
	public static void main(String[] args) {
	
	int chuslo = 9;
			
	 
	  if (3 >= chuslo ) {
		  System.out.println("The number is greater than 3");
	  }
		   else {
			  System.out.println("The number is less than 3");
		   }
	  if (27 <= chuslo) {
		  System.out.println("the number is less than or equal 27");
	  }
		   else {
			  System.out.println("The number is greater than 27");
		   }
	  if (9 == chuslo) {
		  System.out.println("the number is equal 9");
	  }
		   else {
			  System.out.println("The number is not equal 9");
		   }
	  if (9 != chuslo) {
		  System.out.println("the number is not equal 9");
	  }
		   else {
			  System.out.println("The number is equal 9");
		   }
			  
	 if (10 > chuslo)
	  { System.out.println("the number is less than 10");
	  } else {
		  System.out.println("The number is greater than 10");
	  }
	  
	  if (8 < chuslo) {
		  System.out.println("the number is less than 8");
	  }
		   else {
			  System.out.println("The number is greater than 8");
		   }	   
	}

}
